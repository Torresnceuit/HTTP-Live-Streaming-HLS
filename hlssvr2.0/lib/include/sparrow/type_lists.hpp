	struct has_type<type_lists<head, tail>, T>
	{
	private:
		enum{tmp = has_type<tail, T>::value};
	public:
		enum{value = tmp == -1 ? -1 : 1 + tmp};
		typedef typename has_type<tail, T>::result result;
	};

