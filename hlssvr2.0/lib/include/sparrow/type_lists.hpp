	struct null_type{};

	template<typename T, typename U>
	struct type_lists
	{
		typedef T head;
		typedef U tail;
	};
	/////////////////////////////////////////////

	template<typename tlists>
	struct length;

	template<>
	struct length<null_type>{
		enum{ value = 0 };
	};
	template<typename T, typename U>
	struct length<type_lists<T, U> >
	{
		enum{ value = 1 + length<U>::value };
	};
	//////////////////////////////////////////////////////////

	template<typename tlists, size_t index> struct type_at;

	template<typename head, typename tail>
	struct type_at<type_lists<head, tail>, 0>
	{
		typedef head result;
	};

	template<typename head, typename tail, size_t index>
	struct type_at<type_lists<head, tail>, index>
	{
		typedef typename type_at<tail, index - 1>::result result;
	};
